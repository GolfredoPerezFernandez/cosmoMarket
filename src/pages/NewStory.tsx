import { FC, useState, FormEvent, useEffect } from "react";
import axios from "axios";
import Loading from "../components/Loading";
import Button from "@mui/material/Button";
import { ethers } from "ethers";
import { useNavigate } from "react-router-dom";
import { useContractWrite, usePrepareContractWrite, useAccount } from 'wagmi';

import "./NewStory.css";

import {useMoralis} from "react-moralis";

const NewStory: FC = () => {
  const navigate = useNavigate();
  const [blog, setBlog] = useState({ name: "", description: "" });
  const [loading, setLoading] = useState<boolean>(false);
  const [cid, setCid] = useState<string>("");
  const { address } = useAccount();
  const { Moralis, isWeb3Enabled ,enableWeb3} = useMoralis();
  const { config } = usePrepareContractWrite({
    addressOrName: '0xFF71656621BB98E0afB3Bac0d64A2bf7BeFA7C64',
    contractInterface: NFTABI,
    functionName: 'safeMint',
    args: [address, `ipfs://${cid}/metadata.json`],
    overrides: {
      value: ethers.utils.parseEther('1'),
    },
  });
  const {
    isLoading: isContractWriteLoading,
    write
  } = useContractWrite({
    ...config,
    onSuccess:async (res) => {
     
		return res.wait(2).then(async (wait:any) => {
      
          const CootiesSongbird = Moralis.Object.extend("Stories");
          const query = new CootiesSongbird();
      
         
                  query.set("name", blog.name)
                 query.set("description", blog.description)
                   query.set("owner", address?.toString().toLowerCase())
                  query.set("tokenUri", `ipfs://${cid}/metadata.json`)
				  query.set("tokenId",parseInt( wait.logs[0].topics[3]))
                
                    query.set("tokenAddress",'0xFF71656621BB98E0afB3Bac0d64A2bf7BeFA7C64')
					
                     await query.save()
                     
      navigate("/");
      
    
      setLoading(false);
        
       
       
		})
    },
	onError:()=>{
		setLoading(false);
	}
  });

  //upload blog content and nft metadata to ipfs and mint
  const uploadFile = async (event: FormEvent<HTMLFormElement>) => {
    event.preventDefault();

    try {
      setLoading(true);
      const { data } = await axios.post(`${process.env.REACT_APP_BACKEND}/uploadWeb3Storage`, {
        blog
      });
      setCid(data?.cid);
    } catch (error) {
      setLoading(false);
    }
  };

  useEffect(() => {
    async function init(){
await enableWeb3()
  }
  if(!isWeb3Enabled){
    init()
  }
  }, [isWeb3Enabled]);
  useEffect(() => {
    if (cid !== "" && write) {
      console.log(cid);

		write?.();
	  }
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [cid, write]);

  return (
    <>
      {loading && (
        <Loading open={loading || isContractWriteLoading} />
      )}
      <div>
        <form onSubmit={uploadFile} className="writeForm">
          <div className="writeFormGroup">
            <input
              className="writeInput"
              placeholder="Title"
              type="text"
              autoFocus={true}
              value={blog?.name}
              disabled={loading || isContractWriteLoading}
              onChange={(e) => setBlog({ ...blog, name: e.target.value })}
            />
          </div>
          <div className="writeFormGroup">
            <textarea
              className="writeInput writeText"
              placeholder="Tell your story..."
              autoFocus={true}
              rows={5}
              value={blog?.description}
              onChange={(e) => setBlog({ ...blog, description: e.target.value })}
              disabled={loading || isContractWriteLoading}
              style={{ marginTop: "1rem" }}
            />
          </div>
          <Button color="secondary" variant="contained" type="submit">
            Publish
          </Button>
        </form>
      </div>
    </>
  );
};

export default NewStory;


